package day5;

//Short hand and reassignment Operators//

public class ShortHandOperator {

	public static void main(String [] arg) {
		
		
		
		int i = 10;
		
		System.out.println( " The Score is " +i);
		
		i +=5; // i=15 ;
		System.out.println( " The Score is " +i);
		
		i-=2; //i=13
		System.out.println( " The Score is " +i);
		
		i *=10;
		System.out.println( " The Score is " +i);
		
		i/=5;
		
		System.out.println( " The Score is " +i);
		
		i%=7;
		
		System.out.println( " The Score is " +i);
	}
}



