public class Increment$Decrement {

	public static void main(String [] arg) {
		int i =10;
				
		System.out.println( " The Score is " +i);
		
		i = i + 1;
		System.out.println(i);
		
		i +=1 ;
		System.out.println(i);
		
		i++ ;
		System.out.println(i);
		
		++i;
		System.out.println( i++ );
	
	}
	
}
