package day23_nested_loop_array;

public class ArrayLength {
	public static void main(String[] args) {
		
		//declare array names that store 5 names
		
		String [] names = new String [] {"Hatice","Kerem", "Selimcan", "Yavuz", "Sultan", "Hasan"};
		
		String name = names[2];
		
		System.out.println(names[2]);
		System.out.println(names.length); // will give me the number of items in names array ( use length to calculate the size of array
		
		System.out.println(name.length()); // will give me the number of characters in the name index 2
		
		
		
		
		
		
	}
}
